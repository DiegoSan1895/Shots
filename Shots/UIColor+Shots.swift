import UIKit

extension UIColor{
    
    class func backgroundColor() -> UIColor{
        return UIColor(red:0.15, green:0.15, blue:0.15, alpha:1)
    }
    
    class func shotsThemeColor() -> UIColor{
        return UIColor(red:0.28, green:0.98, blue:0.85, alpha:1)
    }
}
